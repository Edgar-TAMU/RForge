namespace RForge.Abstractions.DropDowns;

/// <summary>
/// The direction to open the drop down.
/// </summary>
public enum RfDropDownPosition
{
    /// <summary>
    /// Opens the drop down to the left and downwards.
    /// </summary>
    LeftDown,
    /// <summary>
    /// Opens the drop down to the right and downwards.
    /// </summary>
    RightDown,
    /// <summary>
    /// Opens the drop down to the left and upwards.
    /// </summary>
    LeftUp,
    /// <summary>
    /// Opens the drop down to the right and upwards.
    /// </summary>
    RightUp,
}
