namespace RForge.Abstractions.Notifications;

/// <summary>
/// The location to display the notification relative to the notification manager's parent.
/// </summary>
public enum RfNotificationPosition
{
    /// <summary>
    /// Display the notification at the top center.
    /// </summary>
    TopCenter,
    /// <summary>
    /// Display the notification at the top right.
    /// </summary>
    TopRight,
    /// <summary>
    /// Display the notification at the top left.
    /// </summary>
    TopLeft,
    /// <summary>
    /// Display the notification at the bottom center.
    /// </summary>
    BottomCenter,
    /// <summary>
    /// Display the notification at the bottom right.
    /// </summary>
    BottomRight,
    /// <summary>
    /// Display the notification at the bottom left.
    /// </summary>
    BottomLeft
}

